    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        // TODO add your handling code here:
        // Creamos un JFileChooser para poder seleccionar archivos
        JFileChooser fileChooser = new JFileChooser(); 
        /*
        Creamos un filtro a partir de un nuevo FileNameExtension donde solo vamos a poder mostrar archivos
        de tipo "jpg", "jpeg" y "png".
        Este filtro le establecemos en el jFileChooser para que solo se puedan seleccionar archivos con las 
        extensiones indicadas
        */
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de Imagen", "jpeg", "jpg", "png");
        fileChooser.setFileFilter(filtro);
        // La variable  selec la usamos para ver si el usuario seleccion√≥ un archivo o 
        int selec = fileChooser.showOpenDialog(this);
        // Si selecciona alg√∫n archivo 
        if (selec == JFileChooser.APPROVE_OPTION) {
            String pathImagenEvento = fileChooser.getSelectedFile().getAbsolutePath(); // Obtenemos la ruta de la imagen
            // Creamos un ImageIcon y escalamos la imagen
            ImageIcon imageIcon = new ImageIcon(new ImageIcon(pathImagenEvento).getImage().getScaledInstance(320, 260, Image.SCALE_DEFAULT));
           // La ponemos en el jLabel donde queremos que aparezca
            IconoImagen.setIcon(imageIcon);
            RutaImagen.setText(pathImagenEvento);

        }
    }//GEN-LAST:event_AgregarActionPerformed

