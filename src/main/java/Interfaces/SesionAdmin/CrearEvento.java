    private void RegresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegresarActionPerformed
       
        //Este es el metodo que se encargar√° de guardar el evento si no ocurre ninguna excepcion
        
        try {
                if (HuecosVacios()) { //Excepcion que se lanza si el metodo huecos vacios encuentra algun campo vacio y lanza el mensaje de ExcepcionesHuecosVacios en forma de Panel de mensaje
                    throw new ExcepcionesHuecosVacios();
                }
                if (TituloTxt.getText().length() < 1) { //Excepcion que se lanza si el titulo tiene una longitud de letras menos a 1 y lanza el mensaje de ExcepcionesEventos en forma de Panel de mensaje
                    throw new ExcepcionesEventos(ExcepcionesEventos.TITULO_INCORRECTO);
                }
                
                //Si no salta ninguna excepcion se procederia a atribuir los datos de cada campos con los del Constructor de la clase evento y direccion
                
                String Titulo = TituloTxt.getText();
                String Tipo = ComboBoxTipo.getSelectedItem().toString();
                String Calle = Calletxt.getText();
                String Numero = NumeroTxt.getText();
                String Codigopostal = Codigopostaltxt.getText();
                String Ciudad = Ciudadtxt.getText();
                String Fecha = Fechatxt.getText();
                String Hora = Horatxt.getText();
                String Precio = Preciotxt.getText();
                String Portada = RutaImagen.getText().trim();
                double Calificacion = 0;
                   
                Direccion D1 = new Direccion(Calle, Numero, Ciudad, Codigopostal);
                
                Evento e1 =  new Evento(Titulo, Tipo, D1, Fecha, Hora, Precio ,Portada, Calificacion);
                
                //En esta parte si el Usuario creado cumple con todos los requisitos se le dar√° de alta y saldra el mensaje de Evento Dado 
                //de alta y si por casualidad ya hay un evento creado asi se comparara y saldra el mensaje de Error al Dar de Alta
                
                if (UtilJavaEventsEventos.AltaEvento(e1)) {
                JOptionPane.showMessageDialog(this, "Evento dado de alta.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                    UtilJavaEventsEventos.guardarDatosEventos();
               
            } else {
                JOptionPane.showMessageDialog(this, "Error al dar de alta.", "Mensaje", JOptionPane.ERROR_MESSAGE);
            }
        }catch (ExcepcionesHuecosVacios e) {
            JOptionPane.showConfirmDialog(this, e.getMessage(), "Huecos vacios", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);  
                }
        catch (ExcepcionesEventos e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_RegresarActionPerformed

