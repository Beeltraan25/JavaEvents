    //Metodo el cual verifica si hay algun hueco vacio de todo el JFrame, y manda una señal a la ExcepcionHuecosVacios 
    //que se encarga de mandar el mensaje puesto.
    
    
    private boolean HuecosVacios() { 
        return (Nombretxt.getText().isEmpty() ||
                Correotxt.getText().isEmpty() ||
                Clavetxt.getText().isEmpty() ||
                FechaCaducidadtxt.getText().isEmpty() ||
                Teléfonotxt1.getText().isEmpty()||
                Titulartxt.getText().isEmpty() ||
                NumeroTarjetatxt.getText().isEmpty() ||
                calletxt.getText().isEmpty()||
                numerotxt.getText().isEmpty()||
                CodigoPostaltxt.getText().isEmpty()||
                Ciudadtxt.getText().isEmpty());
        }
