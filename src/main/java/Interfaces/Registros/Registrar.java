    private void IngresarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarBotonActionPerformed
        
        try {
            
            
            
            //Excepcion que se lanza si cualquiera de los huecos de todo el frame estan vacios y se verifica con el metodo HuecosVacios()
            
            
            if (HuecosVacios()) { 
                throw new ExcepcionesHuecosVacios();
            }
            
            
            
            //Excepcion que se lanza segun el metodo esCorreoValido() el cual se encarga de validar el correo
            
            
            if (!UtilJavaEventsClientes.esCorreoValido(Correotxt.getText())) {
                throw new ExcepcionesCliente(ExcepcionesCliente.CORREO_ERRONEO);
            }
            
            
            
            //Excepcion que se lanza segun el metodo esContraseñaValida() el cual se encarga de validar la contraseña
            
            
            if (!UtilJavaEventsClientes.esContrasenaValida(Clavetxt.getText())) {
                throw new ExcepcionesCliente(ExcepcionesCliente.CLAVE_ERRONEO);
            }
            
            
            
            //Excepcion que verifica si el numero de Telefono es valido y cumple con el formato introducido
            
            
            else if (Teléfonotxt1.getText().length() != 12 || Teléfonotxt1.getText().equals("            ")) {
                throw new ExcepcionesCliente(ExcepcionesCliente.TELEFONO_ERRONEO);
            }
            
            
            
            //Excepcion que verifica si el numero de la Tarjeta es valido y cumple con el formato introducido 
            
            
            else if (NumeroTarjetatxt.getText().length() != 19 || NumeroTarjetatxt.getText().equals("    -    -    -    ")) {
                throw new ExcepcionesCliente(ExcepcionesCliente.TARJETADECREDITO_ERRONEA);
            }
            
            
            
            
            //Crea los atributos que se guardaran dentro del cliente y que sera guardado dentro del archivo y ArrayList eventos
             
                String Nombre = Nombretxt.getText();
                String Correo = Correotxt.getText();
                String Clave = Clavetxt.getText();
                String Telefono = Teléfonotxt1.getText();
                String NombreTitular = Titulartxt.getText();
                String Numerotarjeta = NumeroTarjetatxt.getText();
                String Fechacaducidad = FechaCaducidadtxt.getText();
                String Calle = calletxt.getText();
                String Numero = numerotxt.getText();
                String Codigopostal = CodigoPostaltxt.getText();
                String ciudad = Ciudadtxt.getText();
                
                
        
            //Crea la tarjeta, direccion y el cliente que se guardara en la ArrayList de eventos una vez pasada todas las excepciones.  
                
                TarjetaDeCredito TC1 = new TarjetaDeCredito (NombreTitular, Numerotarjeta, Fechacaducidad);
                Direccion D1 = new Direccion (Calle, Numero, Codigopostal, ciudad); 
                Cliente c1 = new Cliente(Nombre,Correo,Clave,Telefono,D1, TC1, false);
                
                
            if (UtilJavaEventsClientes.AltaCliente(c1)) { //Este if se encarga de llamar al metodo AltaCliente que añade el cliente creado en la linea anterior y lo añade a la ArrayList mandando el mensaje
                JOptionPane.showMessageDialog(this, "Cliente dado de alta.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                UtilJavaEventsClientes.guardarDatosClientes();
                
                
               
            } else { //Si el if anterior no se produce por que ya haya un evento creado, se manda el mensaje de Error al dar de alta.
                JOptionPane.showMessageDialog(this, "Error al dar de alta.", "Mensaje", JOptionPane.ERROR_MESSAGE);
            }
            
            
        }
        catch(ExcepcionesHuecosVacios ehv) { //Catch Encargado de mandar el mensaje de error de forma de JOption cuando se produzca la excepcion HuecosVacios
            JOptionPane.showConfirmDialog(this, ehv.getMessage(), "Error en el registro", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        
        
        catch (ExcepcionesCliente ec) {  //Catch Encargado de mandar el mensaje de error de forma de JOption cuando se produzca la excepcion ExcepcionesClientes
            JOptionPane.showConfirmDialog(this, ec.getMessage(), "Error en el registro", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        
        
        catch (Exception e){ //Catch Encargado de mandar el mensaje de error cuando se produzca un error en el codigo dentro del try
            JOptionPane.showConfirmDialog(this, "Error: " + e.toString(), "E Exception", JOptionPane.DEFAULT_OPTION,JOptionPane.ERROR_MESSAGE); 
        }          
    }//GEN-LAST:event_IngresarBotonActionPerformed
    
